bool TempleteTypeEnumeration::showDialog(QMap<QString, QString>* map, bool isTempleteEdit)
{

	map->insert(TempleteTypeEnumeration::IS_DELETE, TempleteTypeEnumeration::STR_FALSE);
	
	RightClickMenu test;
	//第一个参数 true 表示模板编辑，false 表示病历编辑
	test.showMenu(isTempleteEdit, map);
	if (map->contains(TempleteTypeEnumeration::IS_DELETE))
	{
		QString isDelete = map->value(TempleteTypeEnumeration::IS_DELETE);
		if (isDelete == TempleteTypeEnumeration::STR_TRUE)
		{
			return true;
		}
		else
		{
			return false;
		}
	}
	return false;
}
